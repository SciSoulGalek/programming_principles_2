x = 5
print(type(x))

#result: int

x = "Hello World"
print(type(x))

#result: str

x = 20.5
print(type(x))

#result: float

x = ["apple", "banana", "cherry"]
print(type(x))

#result: list

x = ("apple", "banana", "cherry")
print(type(x))

#result: tuple

x = {"name" : "John", "age" : 36}
print(type(x))

#result: dict

x = True
print(type(x))

